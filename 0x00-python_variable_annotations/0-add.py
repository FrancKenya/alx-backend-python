#!/usr/bin/env python3
"""
This module provides a type-annotated function for addition.
"""


def add(a: float, b: float) -> float:
    """
    Function tha t returns the sum of two float numbers.

    Args:
        a (float): The first number.
        b (float): The second number.

    Returns:
        float: The sum of the two numbers.
    """
    return a + b
