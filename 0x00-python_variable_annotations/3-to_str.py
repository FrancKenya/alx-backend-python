#!/usr/bin/env python3
"""
This module provides a type-annotated function
to convert a float to its string representation.
"""


def to_str(n: float) -> str:
    """
    A function that converts a float to its string representation.

    Args:
        n (float): The float to convert.

    Returns:
        str: The string representation of the float.
    """
    return str(n)
